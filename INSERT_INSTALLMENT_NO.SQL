CREATE OR REPLACE PROCEDURE INSERT_INSTALLMENT_NO
AS
CURSOR CON_CURS IS
SELECT *FROM CONTRACTS;
V_MONTHS NUMBER;
V_INSALLMENT_NO NUMBER;

BEGIN

FOR CON_RECORD IN CON_CURS LOOP
  
  V_INSALLMENT_NO :=CALC_INSTAALMENT_NO(CON_RECORD);--THIS FUNCTION TO CALC NUMBER OF INSALLTMENT NUMBER
   
   UPDATE CONTRACTS 
   SET PAYMENTS_INSTALLMENTS_NO=V_INSALLMENT_NO
    WHERE CONTRACT_ID=CON_RECORD.CONTRACT_ID;

END LOOP;
END;
SELECT *FROM USER_ERRORS;