CREATE OR REPLACE FUNCTION CALC_AMOUNT_MONTHADDED(CON_RECORD CONTRACTS%ROWTYPE,V_AMOUNT OUT NUMBER)
RETURN NUMBER

IS
V_ADDED_MONTH NUMBER;



BEGIN

V_AMOUNT:=(CON_RECORD.CONTRACT_TOTAL_FEES-NVL(CON_RECORD.CONTRACT_DEPOSIT_FEES,0))/CON_RECORD.PAYMENTS_INSTALLMENTS_NO;

 IF CON_RECORD.CONTRACT_PAYMENT_TYPE ='ANNUAL'THEN
        V_ADDED_MONTH:=12;
   ELSIF CON_RECORD.CONTRACT_PAYMENT_TYPE ='QUARTER'THEN
        V_ADDED_MONTH:=3;
    ELSIF CON_RECORD.CONTRACT_PAYMENT_TYPE ='MONTHLY'THEN
        V_ADDED_MONTH:=1;
    ELSE
           V_ADDED_MONTH:=6;
    END IF;
    RETURN
    V_ADDED_MONTH;

END;
